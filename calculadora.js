let multiplica;
let dividi;
let soma;
let subtrai;

function calcula(num1, num2) {
    multiplica = num1 * num2;
    dividi = num1 / num2;
    soma = num1 + num2;
    subtrai = num1 - num2;
    return;
}

calcula(10, 40);

console.log(multiplica);
console.log(soma);
console.log(dividi);
