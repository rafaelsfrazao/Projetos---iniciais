const classe = "Druida";

switch (classe) {
    case "Guerreiro":
        console.log("Você escolheu a classe Guerreiro!");
        break;
    case "Mago":
        console.log("Você escolheu a classe Mago!");
        break
    case "Arqueiro":
        console.log("Você escolheu a classe Arqueiro!")
        break
    default:
        console.log("Classe inválida! Escolha entre Guerreiro, Mago ou Arqueiro.");
        break;
}